"use strict";
import axios from "axios-https-proxy-fix"; // in case proxy support is needed using library w/ proxy fix

export default async function scoreScraper(movieTitle, movieYear) {

    let html = await getGooglePage(`${movieTitle} (${movieYear}) movie`)

    let imdbRating = "";
    let rtRating = "";

    let ratingsSection = html.substring(html.indexOf('<a class="TZahnb vIUFYd"'), html.indexOf('title="Rotten Tomatoes"'));

    imdbRating = ratingsSection.substring(ratingsSection.indexOf('class="gsrt KMdzJ"') + 38, ratingsSection.indexOf('/10') + 3);

    rtRating = ratingsSection.substring(ratingsSection.lastIndexOf('class="gsrt KMdzJ"') + 38, ratingsSection.indexOf('%</span') + 1);

    if (imdbRating === "" || imdbRating.length > 6) {
        imdbRating = "N/A";
    }

    if (rtRating === "" || rtRating.length > 3) {
        rtRating = "N/A";
    }

    return [imdbRating, rtRating]
}

async function getGooglePage(title) {

    let userAgent = randomUserAgent();
    let data = "";

    let response = await axios.get(`https://www.google.com/search?q=${title}`, { headers: { 'User-Agent': `${userAgent}` } });
    data = response.data;

    return data;
}

